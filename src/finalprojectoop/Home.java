    private void btnCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCartActionPerformed
        try {                                        
            // Nilai Item
            String strVaS = txtShoes.getText();
            int valueS = Integer.parseInt(strVaS);
            String strVaJ = txtJeans.getText();
            int valueJ = Integer.parseInt(strVaJ);
            String strVaSo = txtSocks.getText();
            int valueSo = Integer.parseInt(strVaSo);
            String strVaP = txtPb.getText();
            int valueP = Integer.parseInt(strVaP);
            String strVaT = txtTs.getText();
            int valueT = Integer.parseInt(strVaT);
            String strVaL = txtLs.getText();
            int valueL = Integer.parseInt(strVaL);
            
            //Harga Item
            int priceShoes = 350000;
            int priceSocks = 50000;
            int priceJeans = 150000;
            int pricePb = 60000;
            int priceTs = 120000;
            int priceLs = 145000;
            
            
            int localTotal = 0;
            
            
            
            if (valueJ>0||valueL>0||valueP>0||valueS>0||valueSo>0||valueT>0) {
                Connection conn = (Connection)finalprojectoop.connect.getConnection();
                Statement s = conn.createStatement();
                //Sepatu
                if(valueS>0){
                    String name = "Red Shoes";
                    int x =insertToTemp(name, priceShoes, valueS,conn,s);
                    localTotal += x;
                }
                //Blue Jeans
                if(valueJ>0){
                    String name = "Blue Jeans";
                    int x =insertToTemp(name, priceJeans, valueJ ,conn,s);
                     localTotal += x;
                }

                //Blue socks
                if(valueSo>0){
                    String name = "Blue Socks";
                    int x =insertToTemp(name, priceSocks, valueSo ,conn,s);
                     localTotal += x;
                }
                //Pouch Bag
                if(valueP>0){
                    String name = "Pouch Bag";
                    int x =insertToTemp(name, pricePb, valueP ,conn,s);
                     localTotal += x;
                }
                //Black TS
                if(valueT>0){
                    String name = "Black T-shirt";
                    int x =insertToTemp(name, priceTs, valueT ,conn,s);
                     localTotal += x;
                }
                //LS
                if(valueL>0){
                    String name = "Long Shirt";
                    int x =insertToTemp(name, priceLs, valueL ,conn,s);
                     localTotal += x;
                }
                System.out.println(localTotal);
                String x = String.valueOf(localTotal);
                TransactionDetails newDetails = new TransactionDetails();
                TransactionDetails.txtTotal.setText(x);
                newDetails.setVisible(true);
                this.dispose();   
            }else{
                JOptionPane.showMessageDialog(this, "You haven't buy anyone yet" );
            }
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCartActionPerformed

