    private int insertToTemp(String name, int price, int qty ,Connection conn,Statement s) throws SQLException{
        int sum = price*qty;
        
        try {
        String sql = "INSERT INTO transaction_temp (Date,Name, Price,Qty,Sum) VALUES (?,?,?,?,?)";
        Timestamp date = new Timestamp(new java.util.Date().getTime());
            try (java.sql.PreparedStatement pst = conn.prepareStatement(sql)) {
                pst.setTimestamp(1, date);
                pst.setString(2, name);
                pst.setInt(3, price);
                pst.setInt(4, qty);
                pst.setInt(5, sum);
                pst.execute();
            }
        } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e);
        }
        return sum;
    }

